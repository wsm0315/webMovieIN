package model;

public class RewardVO {
	private int seq_PID;
	private int std_invest;
	private String reward;
	
	public int getSeq_PID() {
		return seq_PID;
	}
	public void setSeq_PID(int seq_PID) {
		this.seq_PID = seq_PID;
	}
	public int getStd_invest() {
		return std_invest;
	}
	public void setStd_invest(int std_invest) {
		this.std_invest = std_invest;
	}
	public String getReward() {
		return reward;
	}
	public void setReward(String reward) {
		this.reward = reward;
	}
	
}
